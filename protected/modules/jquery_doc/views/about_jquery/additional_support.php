<h1>Additional jQuery Support</h1>

<p>While we hope to cover most jQuery-related topics on this site, you may need additional or more immediate support. The following resources can prove useful.</p>



<hr/>
<h2>Official Forums</h2>

<p><a href="http://forum.jquery.com/">http://forum.jquery.com/</a></p>
<p>There are many subforums where you can discuss jQuery, ask questions, talk about JavaScript, or announce your plugins.</p>

<ul>
    <li><a href="http://forum.jquery.com/getting-started">Getting Started</a>
        <ul><li>This is the best place to post if you are brand new to jQuery and JavaScript.</li></ul>
    </li>
    
    <li><a href="http://forum.jquery.com/using-jquery">Using jQuery</a>
        <ul>
            <li>This is the best place to post if you have general questions or concerns.</li>
            <li>If you've built a site that uses jQuery, or would like to announce a new plugin, this is the place to do it.</li>
        </ul>
    </li>
    
    <li><a href="http://forum.jquery.com/using-jquery-plugins">Using jQuery Plugins</a>
        <ul><li>If you are a plugin author or user and you wish to discuss specific plugins, plugin bugs, new features, or new plugins.</li></ul>
    </li>
    
    <li><a href="http://forum.jquery.com/using-jquery-ui">Using jQuery UI</a>
        <ul><li>This is the place to discuss use of <a href="http://jqueryui.com/">jQuery UI</a> Interactions, Widgets, and Effects</li></ul>
    </li>
    
    <li><a href="http://forum.jquery.com/jquery-mobile">jQuery Mobile</a>
        <ul><li>This is the place to discuss jQuery Mobile.</li></ul>
    </li>
    
    <li><a href="http://forum.jquery.com/developing-jquery-core">Developing jQuery Core</a>
        <ul>
            <li>This forum centers around development of the jQuery library itself.</li>
            <li>Post here if you have questions about certain bugs, development with jQuery, features, or anything in the bug tracker or Git.</li>
        </ul>
    </li>
    
    <li><a href="http://forum.jquery.com/developing-jquery-plugins">Developing jQuery Plugins</a>
        <ul><li>This forum covers development of jQuery plugins.</li></ul>
    </li>
    
    <li><a href="http://forum.jquery.com/developing-jquery-ui">Developing jQuery UI</a>
        <ul>
            <li>This is the place to discuss development of <a href="http://jqueryui.com/">jQuery UI</a> itself – including bugs, new plugins, and how you can help.</li>
            <li>All jQuery UI svn commits are posted to this list to facilitate feedback, discussion, and review.</li>
            <li>Also note that a lot of the development and planning of jQuery UI takes place on the <a href="http://wiki.jqueryui.com/">jQuery UI Development and Planning Wiki</a>.</li>
        </ul>
    </li>
    
    <li><a href="http://forum.jquery.com/developing-jquery-mobile">Developing jQuery Mobile</a>
        <ul><li>This forum covers issues related to the development of jQuery Mobile.</li></ul>
    </li>
    
    <li><a href="http://forum.jquery.com/qunit-and-testing">QUnit and Testing</a>
        <ul><li>This is the place to discuss JavaScript testing in general and QUnit in particular</li></ul>
    </li>
</ul>

<p>At the bottom of each of the forums is an RSS feed you can subscribe to.</p>

<p>To ensure that you'll get a useful answer in no time, please consider the following advice:</p>

<ul>
    <li>Ensure your markup is valid.</li>
    <li>Use Firebug/Developer Tools to see if you have an exception.</li>
    <li>Use Firebug/Developer Tools to inspect the HTML classes, CSS, etc.</li>
    <li>Try expected resulting HTML and CSS without JavaScript/jQuery and see if the problem could be isolated to those two.</li>
    <li>Reduce to a minimal test case (keep removing things until the problem goes away, etc.)</li>
    <li>Provide that test case as part of your mail. Either upload it somewhere or post it on <a href="http://jsbin.com/">jsbin.com</a>.</li>
</ul>

<p>In general, keep your question short and focused and provide only essential details – others can be added when required.</p>



