<h2>StackOverflow</h2>

<p>There is an active and well-informed support community at <a href="http://stackoverflow.com/questions/tagged/jquery">StackOverflow</a>. You can likely find an answer for whatever issue you're experiencing. If your question isn't addressed, you can ask a new question and often receive a quick response.</p>