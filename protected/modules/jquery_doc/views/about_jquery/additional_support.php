<h2>Chat / IRC Channel</h2>

<p>jQuery also has a very active IRC channel, <code>#jquery</code>, hosted by <a href="http://freenode.net/">freenode</a>.</p>

<p>The IRC Channel is best if you need quick help with any of the following:</p>

<ul>
    <li>JavaScript</li>
    <li>jQuery syntax</li>
    <li>Problem solving</li>
    <li>Strange bugs</li>
</ul>

<p>If your problem is more in-depth, we may ask you to post to the mailing list, or the bug tracker, so that we can help you in a more-suitable environment.</p>

<p><b>Connect info:</b></p>
<p><b>Server:</b> irc.freenode.net</p>
<p><b>Room:</b> <code>#jquery</code></p>

<p>You can also connect at <a href="http://webchat.freenode.net/?channels=#jquery">http://webchat.freenode.net/?channels=#jquery</a>.</p>

<p>If you wish to post code snippets to the channel, you should use a paste site, like <a href="http://jsfiddle.net/">jsfiddle.net</a> or <a href="http://jsbin.com/">jsbin.com</a>.</p>

<p>Additional info regarding jQuery's use of IRC can be found on <a href="http://irc.jquery.org">irc.jquery.org</a>.</p>



