<h1>Transformation</h1>


<div style="" id="wikipage-inner">
<p>This tool will eventually be used, so best I mention it.</br>
It's not a hard tool to use, since you've probably used it in some sort of way in some other program.</br>
Basically you can free transform with your mouse or you can mathematically transform (I do the latter more often since it enables you to maintain proportions a lot easier).</br>
If, by some chance, you don't know how to use it, just click and drag one of the boxes on the sides and/corners into the direction you want to transform.</br>
Dragging the box closer to the center of the image will make it smaller and dragging it farther from the center will make it bigger.</br>
This occurs in a few other tools as well, like cropping.</p>

