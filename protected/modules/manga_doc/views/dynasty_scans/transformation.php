<p><span style="line-height:15px;">X: Location on the x-coordinate</span></br>
<span style="line-height:15px;">Y: Location on the y-coordinate</span></p>

<p><span style="line-height:15px;">W: Width, in percent of when you initiated the transformation tool on the image</span></br>
<span style="line-height:15px;">8: Lock aspect ratio</span></br>
<span style="line-height:15px;">H: Height, in percent</span></p>

<p><span style="line-height:15px;">&lt;: Rotation clockwise, in degrees</span></br>
<span style="line-height:15px;">H: Horizontal skew (change and see for yourself)</span></br>
<span style="line-height:15px;">V: Vertical skew (change and see for yourself)</span></p>
    
<p><span style="line-height:15px;">I must and will stress this: <strong>do not use this tool more than once on any rasterized image, particularly when resizing upwards</strong>.</span></br>
<span style="line-height:15px;">In general, resizing up is a bad thing to do anyway (only use in desperate situations).</span></br>
<span style="line-height:15px;">It will cause blurring and distortions, depending on whether or not the transforms were proportional.</span></br>
<span style="line-height:15px;">Either transform it right the first time or start over with the original.</span></p>

