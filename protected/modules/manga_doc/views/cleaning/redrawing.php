<h1>Redrawing</h1>


<p>The process of redrawing is quite possibly one of the hardest part of editing so what we're going to do here is give you a couple examples of before/after work that I have done and then we'll move on to actual walk through examples so you can do a little practicing yourself. The important thing to remember when doing reconstruction is that you only need to reconstruct what can be seen! Meaning... if you have text going over the area that you're reconstructing then there's no reason to spend time fixing it, because you won't see it! One of the skills that's vital is the ability to place the text in the right spot so you give yourself the least amount of work possible. You're going to run into redraws. As much as I hate to say it, you will. Below is a handful of examples. </p>

