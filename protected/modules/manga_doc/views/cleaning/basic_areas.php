<h1>Basic Areas</h1>


<p>We'll start with the easy part of cleaning. :) The goal here is to remove everything that can be removed easily. Meaning you don't have to reconstruct around it after removing it. We'll get into reconstruction later! Make sure you select your BOTTOM LAYER and that the top one is still invisible. First thing you want to do is select a hard tip brush. Make sure it's solid so you're not spraying white all over the edges of the bubble lines. Set your brush to whatever size you may need to easily control. I usually set mine very large because I'm used to doing this. It might be slightly easier to use a smaller size tip if you're having problems accidentally erasing bubble edges and things you don't want to erase. In the sample below you'll see on the left the original and on the right what I erased. <b><i>Note: Notice I alo erased the black specs that weren't taken care of by leveling, they're circled in red.</i></b></p>

