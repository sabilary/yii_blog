<h1>Do's and Don'ts</h1>

<p><b>All that's great but give me the basics!</b></p>

<p>Okay, sorry, sometimes I'm a little long-winded! Here's some things to remember, in no particular order.</p>

<ul>
    <li><span style="font-size:13px;line-height:1.5em;">Different characters do no get different fonts.</span></li>
        <ul>
            <li>
                <span style="line-height:1.5em;">Character 1 and character 2 will both be using the same dialog font. The only&nbsp;</span>circumstance<span style="line-height:1.5em;">&nbsp;this shouldn't be followed is if the actual raw copy differentiates font styles from one character to the next. I've edited&nbsp;</span>hundreds<span style="line-height:1.5em;">&nbsp;of chapters of manga and I've only encountered this once. So don't look for it to happen often (if ever!)</span>
            </li>
        </ul>
</ul>

<ul>
    <li><span style="line-height:1.5em;">Once you pick a font for handwritten or narration or thought for the manga, you stick with it.</span></li>
        <ul>
            <li>
                <span style="line-height:1.5em;">&nbsp;</span><span style="font-size:13px;line-height:1.2em;">So often we as editors go through long spurts where we don't want to edit. It's a creative thing, I get it. Then one day we wake up and we can blow through a whole edit in a few hours. Make sure that if you stop part way through an edit that when you start back up again you use the same font you used to begin with. Often you'll see a sudden change halfway through a chapter where all the narration or all the handwritten text suddenly becomes something else and stays that way. Try creating yourself a .txt file wen you begin an edit and each time you encounter a new font, write it down in your .txt file so you can keep track of which fonts are used and where to use them.</span>
            </li>
        </ul>
</ul>

<ul>
    <li><span style="font-size:13px;line-height:1.2em;">Vertical text.</span></li>
        <ul>
            <li>
                <span style="line-height:1.2em;">Very very rarely will you ever typeset anything vertically. There's always&nbsp;</span><span style="line-height:15.600000381469727px;">exceptions</span><span style="line-height:1.2em;">&nbsp;but it's basically a no-no if you can avoid it. This includes punctuation such as ..... and long dashes â€”. You'll see a lot of groups allow things like the examples below. We do <em><strong>not. </strong></em>We expect these to be removed and typeset horizontally.</span>
            </li>
        </ul>
</ul>

