<ul>
    <li><span style="line-height:15.600000381469727px;">Do not use handwritten font except for handwritten font. (hah)</span></li>
        <ul>
            <li><span style="line-height:15.600000381469727px;">Another issue you'll see a lot is groups using handwritten font for an entire chapter. It simply doesn't belong. Handwritten font should be reserved for ONLY handwritten text in the raw.</span>
            </li>
        </ul>
</ul>

<ul>
    <li><span style="line-height:15.600000381469727px;">Don't take it upon yourself to&nbsp;emphasis&nbsp;words.&nbsp;</span></li>
        <ul>
            <li>
                <span style="line-height:15.600000381469727px;">I know the way we read things in our heads isn't always the same as the translators portray them. That's OK! But please don't take it upon yourself to bold certain bubbles because you feel like they would be conveyed better that way. Our translators are very skilled and if the bubble should be bolded they'll denote it in the script.&nbsp;</span>
            </li>
        </ul>
</ul>

<ul>
    <li><span style="line-height:15.600000381469727px;">Don't oversize your font.</span></li>
        <ul>
            <li>
                <span style="line-height:15.600000381469727px;">&nbsp;</span><span style="font-size:13px;line-height:1.2em;">Just because you have a huge bubble and only a small bit of text doesn't mean you need to increase the size of the font to fill the bubble. Often this looks very bad.</span>
            </li>
        </ul>
</ul>

<ul>
    <li><span style="font-size:13px;">Font colors</span></li>
        <ul>
            <li>
                Occasionally you'll run into stuff that's marked by the translators as "flashback" font. This is the only scenario where we allow you to lighten the font to a grey color. It's not necessary but it seems common&nbsp;practice&nbsp;by a lot of editors to do so. Personally I prefer just changing the font style (like most mangaka do).&nbsp;
            </li>
        </ul>
</ul>

<ul>
    <li><span style="font-size:13px;line-height:1.5em;">Hyphens</span></li>
        <ul>
            <li>
                <span style="font-size:13px;line-height:1.5em;">Please and I repeat please, if you're unsure of how a word should be broken visit <a href="http://www.merriam-webster.com/">http://www.merriam-webster.com/</a>&nbsp;and look it up. Also make sure that your hyphen is always at the tail end of the first part of the word, never at the beginning of the second.</span><span style="font-size:13px;line-height:1.2em;">&nbsp;</span>
            </li>
        </ul>
</ul>

<ul>
    <li>Copy and Paste Vs. Typing</li>
        <ul>
            <li>
                We recommend that you copy and paste from the script directly into your image editing program. Too often the cause of a misspelling is a typo, not a lack of intelligence. You can do it either way but please be very careful if you plan to type it yourself.&nbsp;
            </li>
        </ul>
</ul>


