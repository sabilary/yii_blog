<h1>How Do I Know Which Fonts Go Where</h1>


<p><b>This script is confusing, it doesn't tell me what font to use!</b></p>

<p>Oh but it does young grasshopper. :) Here I'll explain to you how our scripts work and the meanings of some of the terms we use!</p>

<p>First let's start of by showing you the script key. You can find this on the main page of our group site and as an editor or typesetter it's something you should memorize.</p>

<p>
[...] for translator's notes<br>
-...- for sound effects<br>
*...* for signs and the like<br>
@...@ for handwritten text<br>
_..._ for emphasis, italicized titles, etc.<br>
--- for long lines<br>
(...) for font notes<br>
</p>

<p>Now to explain this I'm going to create a little script example that encompasses them all. Please bare with me, I'm an artist not a writer... >.> You'll very rarely see them all so often but this is for training purposes. Normally your big ones will be sound effects and handwritten text.</p>

<p>
<span style="color:#cc99ff;">
    [...] for translator's notes (This note on the last line is from the translator to the editor or team explaining something that shouldn't be typeset)
</span><br>
<span style="color:#00ccff;">
    -...- for sound effects (All sound effects should be noted with a dash on each end.)
</span><br>
<span style="color:#ff00ff;">
    *...* for signs and the like (Any signs on buildings, bottles, bags, shirts, anything&nbsp;that's&nbsp;an inanimate object with&nbsp;writing&nbsp;on it will be noted with asterisks on each end.)
</span><br>
<span style="color:#339966;">
    @...@ for handwritten text (Almost all&nbsp;handwritten&nbsp;text is small and can be inside or outside of a bubble. You'll need to pick a handwritten font like Augie for these.)
</span><br>
<span style="color:#993366;">
    _..._ for emphasis, italicized titles, etc. (You won't see this one too often. It's when a bubble or in the case of this example, a single word, is bolded or a different font than the rest of the bubble.)
</span><br>
<span style="color:#ff0000;">
    --- for long lines (Depending on the translator this could be - or -- or --- or even — so keep your eyes open for these.)
</span><br>
<span style="color:#ff99cc;">
    (...) for font notes (In this example it says "cute". It can say basically anything. This is the translator telling you the font needs to be changed from the normal dialog and a description of how the font should be styled.)
</span>
</p>

<p>Miku<span style="color:#ff99cc;">(cute)</span>: Where are we going today?</p>

<p>Aya: I don't know but<span style="color:#ff0000;">---</span></p>

<p><span style="color:#00ccff;">-crash-</span></p>

<p><span style="color:#339966;">@I don't know but look up there...@</span></p>

<p><span style="color:#ff00ff;">*DO NOT ENTER* <span style="color:#cc99ff;">[This sign was kind of hard to read so I'm translating it as do not enter because it fits the scene.]</span></span></p>



<hr/>
<p>Some other things to look for in the script are the differences between Narration(a.k.a. Floating Text) and normal text. Here's an example with a matching script for comparison purposes.</p>

<p>Mio: Morning, Chiriko!</p>
 
<p>Chiriko: @Hm?@ …Oh. @It’s you guys…@</p>
 
<p>Chiriko: Looks like you really lived it up over summer break.</p>
 
<p>@The cool crowd should just explode.@</p>
 
<p>Mio: Shut up! @We’re not part of the cool crowd anyway…@</p>
 
<p>-swoosh-</p>
 
<p>Chiriko(thought): Huh?</p>
 
<p>*Reception*</p>
 
<p>Mio: What’s up?</p>
 
<p>Chiriko: Nothing.</p>
 
<p>It seemed like I saw blond hair for a second, but I guess not…</p>
 
<p>So I thought, but…</p>

