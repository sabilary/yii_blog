<p><b>#1.</b> Here you have an example of the basic normal dialog font. Notice in the script it's done in this format... (Name: What they're saying.)</p>

<p><b>#2.</b> I have two #2's circled. Notice how they can be inside or outside of a bubble but they're both the same font and both listed in the script @Like this.@</p>

<p><b>#3.</b> This shows the SFX swoosh. Which will always be listed -like this- They can be -mutiple words- or a -single- word.</p>

<p><b>#4.</b> Notice in the script how the font note is (thought) and the font has changed from the normal dialog font? A good way to be certain a bubble is a thought bubble is by the little tiny bubbles coming off the bubble. Nearly all mangaka style their thought bubbles the same. So if the translator misses the font note please do use the thought font if for some reason you feel it's a thought bubble.</p>

<p><b>#5.</b> This is where it gets tricky. A lot of people get confused with this. This is narration/floating text/whatever you want to call it. It's always going to be listed in the script without a character name in front of it. That's the way for you to tell the difference. If there is no character name in the script before the line then it should be put in the narration font. It can be inside of bubbles, inside of boxes, outside of bubbles, inside burst bubbles. It can be all over the place. Just remember, when it doubt check to see if there's a character name (<b>Mio:</b> Morning, Chiriko!) in front of the text.</p>

