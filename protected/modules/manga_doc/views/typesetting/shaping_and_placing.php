<h1>Shaping and Placing</h1>


<p><b>What do you mean I have to shape it?! >.<;!</b></p>

<p>Another of the most common mistakes and causes of tension between an editor(especially a new one!) and a QCer is the shaping of their text. What do I mean? Let's look at some examples and explain them.</p>

<p align="center"><?php echo CHtml::image(Yii::app()->controller->module->registerImage_tutorial('03-002.png'), "image");?></p>

<p><b>#1.</b> This is what you're looking for. The shape of the font actually closely matches the shape of the bubble. While it does dip in a little in the center it's OK. At Kawaii we actually prefer a little of an hourglass shape to having every other word hyphenated to create a perfect shape. You'll also notice a nice even white halo all the way around the text to the bubbles edge. Try to shape the font as closely to the container you're putting it in as you can. If it's a box, try to make the font a box shape.</p>
 
<p><b>#2.</b> This happens a lot more than I'd like to admit. Just because you have a ton of bubble space does not mean you need to use it all. When typesetting, remember... less is more! Your bubble will be overcrowded and look unprofessional. Try to leave a pretty good edge around the font and the bubble edge.</p>
 
<p><b>#3.</b> In example one I mentioned how we don't mind a little bit of an hourglass shape to a font block. This is when we do mind. Not only is it overcrowded by being so close to the edge of the bubble on the first two lines it's misshapen and looks bad. This block of font does not resemble the shape of the bubble at all.</p>
 
<p><b>#4.</b> And to one of my personal pet peeves. Do not under any circumstance leave a single letter or punctuation on a line by itself to start or end a bubble. It just looks bad. :(</p>
 
<p>There are all kinds of horrible text mistakes you can make but when it doubt, try to shape the block like the bubble the best you can.</p>


<hr/>
<p>
    <b>Now onto placing the actual font!</b><br>
    Tips and tricks and things to look out for follow!
</p>

<p>Remember, your font tool can be used one of two ways. You can simply click in the bubble and paste the text into a line and do your own line breaks manually OR you can click and drag wit the font tool and create a box. I personally prefer the first way. The second way will occasionally create a mess of your font shape. Let's look at some examples of the ways to do it!</p>

<p align="center">
    <b>Option A</b>
</p>
<p align="center">
    <?php echo CHtml::image(Yii::app()->controller->module->registerImage_tutorial('03-003.png'), "image1");?>
</p>

<p>In this example you see how I used the type tool to paste a full like. I then went through and broke the line manually to create the shape that's in the bubble. You can see the red lines I drew showing where I broke it. This can be trial and error sometimes as yo have to break and unbreak the lines multiple times to find the perfect combination to get the font shape you want. While it does give you more control of your shape it also takes a bit more time.</p>

<p align="center">
    <b>Option B</b>
</p>
<p align="center">
    <?php echo CHtml::image(Yii::app()->controller->module->registerImage_tutorial('03-004.png'), "image2");?>
</p>

<p>In this first image you'll see the box that's created when you click and drag with the font tool. It's very much like the selection tool but instead creates a box that's ready to be type din with a word wrap. That means that as you type or even if you paste a line in from the script (which I recommend) it will break the line for you and even hyphenate larger words if they're unable to fit. Clearly the benefits of this are the speed in which you can typeset but the drawback is the control yo have over the shape.
 </p>

<p align="center"><?php echo CHtml::image(Yii::app()->controller->module->registerImage_tutorial('03-005.png'), "image3");?></p>
 
 <p>Here you can see some of the small boxes I circled(squared). These boxes can be click and drug around to change the shape of the font block you have. In the first image you see I made a box the same of the whole bubble. That left all the text at the top of my bubble. So you can either drag the font box down and the text inside with it or you can simply reshape the font box so the text becomes centered vertically like the picture above.</p>
 
 <p>
    <b>How can I make it perfect though?</b>
    Try not to worry about it. The longer you edit the more and more perfect your placement will become. Think of it as an art, not a science. If something is terribly off a quality checker will catch the mistake and have you adjust. Just try to eyeball it and get it as close as possible.
 </p>


